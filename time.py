def create_dictionary(key, value):
    """
    Function to create a dictionary with a single key-value pair.
    
    Parameters:
        key: The key for the dictionary.
        value: The value corresponding to the key.
    
    Returns:
        A dictionary with the provided key-value pair.
    """
    return {key: value}

# Example usage:
my_dict=create_dictionary("key", "value")
print(my_dict)
